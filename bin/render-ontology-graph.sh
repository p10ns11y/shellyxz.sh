#!/usr/bin/env bash
# render-ontology-graph.sh — emit Mermaid flowchart from shell-kernel.graph.yaml
# Usage: bin/render-ontology-graph.sh [--subgraph path|boundary|load_order|all]
set -euo pipefail

GRAPH_FILE="${SHELL_ROOT:-$HOME/.config/shell}/.agents/ontology/shell-kernel.graph.yaml"
SUBGRAPH_FILTER="${1:-all}"
if [[ "${1:-}" == "--subgraph" ]]; then
  SUBGRAPH_FILTER="${2:-all}"
fi

if [[ ! -f "$GRAPH_FILE" ]]; then
  printf 'render-ontology-graph: missing %s\n' "$GRAPH_FILE" >&2
  exit 1
fi

python3 - "$GRAPH_FILE" "$SUBGRAPH_FILTER" <<'PY'
import re
import sys
from collections import defaultdict

graph_path, subgraph_filter = sys.argv[1], sys.argv[2]

text = open(graph_path, encoding="utf-8").read()

# --- nodes: id, type, name, subgraph ---
nodes = {}
current = None
for line in text.splitlines():
    if line.startswith("  - id:"):
        current = {"id": line.split(":", 1)[1].strip()}
        nodes[current["id"]] = current
    elif current is not None and line.strip().startswith("type:"):
        current["type"] = line.split(":", 1)[1].strip()
    elif current is not None and line.strip().startswith("name:"):
        current["name"] = line.split(":", 1)[1].strip()
    elif current is not None and line.strip().startswith("subgraph:"):
        current["subgraph"] = line.split(":", 1)[1].strip()

# --- edges (inline YAML objects) ---
edges = []
for match in re.finditer(
    r"\{ from: ([^,]+), to: ([^,]+), type: ([^,}]+)(?:, note: ([^}]+))?\}",
    text,
):
    edge = {
        "from": match.group(1).strip(),
        "to": match.group(2).strip(),
        "type": match.group(3).strip(),
    }
    if match.group(4):
        edge["note"] = match.group(4).strip()
    edges.append(edge)

def short_id(node_id: str) -> str:
    return node_id.split(":", 1)[-1].replace("PublicHook_", "PH_")

def label(node: dict) -> str:
    name = node.get("name", short_id(node["id"]))
    node_type = node.get("type", "?")
    return f"{name}<br/><small>{node_type}</small>"

def node_subgraph(node_id: str) -> str:
    node = nodes.get(node_id, {})
    if "subgraph" in node:
        return node["subgraph"]
    # Infer from id prefix patterns
    if "Path" in node_id or "Contract" in node_id or "Artifact_" in node_id:
        return "path"
    if "Fn_path" in node_id or "Inv_" in node_id:
        return "path"
    if "LoadOrder" in node_id:
        return "load_order"
    if "Kernel" in node_id or "Verification" in node_id or "Plugin" in node_id:
        return "boundary"
    return "other"

subgraphs = defaultdict(list)
for node_id, node in nodes.items():
    sg = node_subgraph(node_id)
    if subgraph_filter != "all" and sg != subgraph_filter:
        continue
    subgraphs[sg].append(node_id)

if subgraph_filter != "all":
    allowed = set(subgraphs.get(subgraph_filter, []))
    # include edge endpoints for selected subgraph
    for edge in edges:
        if node_subgraph(edge["from"]) == subgraph_filter:
            allowed.add(edge["from"])
            allowed.add(edge["to"])
    nodes_to_emit = allowed
else:
    nodes_to_emit = set(nodes.keys())

print("%% Generated by bin/render-ontology-graph.sh — do not hand-edit")
print("%% Source: .agents/ontology/shell-kernel.graph.yaml")
print("flowchart TB")

edge_styles = {
    "owns": "-->",
    "dependsOn": "-.->|depends|",
    "mustNot": "-.x|mustNot|",
    "documentedIn": "-.->|doc|",
    "resolvesTo": "-->|resolves|",
    "usesLexicon": "-->|lexicon|",
    "enforces": "==>|enforces|",
    "appliesBefore": "-->|before|",
}

for sg_name in sorted(subgraphs.keys()):
    ids = [nid for nid in subgraphs[sg_name] if nid in nodes_to_emit]
    if not ids:
        continue
    safe = re.sub(r"[^a-zA-Z0-9_]", "_", sg_name)
    print(f"  subgraph {safe}[\"{sg_name}\"]")
    for node_id in sorted(ids, key=short_id):
        node = nodes[node_id]
        sid = short_id(node_id)
        print(f'    {sid}["{label(node)}"]')
    print("  end")

print("")
for edge in edges:
    if edge["from"] not in nodes_to_emit or edge["to"] not in nodes_to_emit:
        continue
    arrow = edge_styles.get(edge["type"], "-.->")
    print(f"  {short_id(edge['from'])} {arrow} {short_id(edge['to'])}")

PY
