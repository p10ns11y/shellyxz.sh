#!/usr/bin/env bash
# extract-ontology-facts.sh — scan repo for facts the ontology graph claims.
# Usage: bin/extract-ontology-facts.sh [-o FILE]
# Default output: .agents/ontology/shell-kernel.extracted.yaml (gitignored)
set -euo pipefail

CONFIG_DIR="${SHELL_ROOT:-$HOME/.config/shell}"
OUTPUT_FILE="${CONFIG_DIR}/.agents/ontology/shell-kernel.extracted.yaml"

while [[ $# -gt 0 ]]; do
    case "$1" in
        -o|--output)
            OUTPUT_FILE="${2:?missing output path}"
            shift 2
            ;;
        -h|--help)
            echo "Usage: extract-ontology-facts.sh [-o FILE]"
            exit 0
            ;;
        *)
            echo "extract-ontology-facts: unknown argument: $1" >&2
            exit 1
            ;;
    esac
done

if [[ ! -d "$CONFIG_DIR" ]]; then
    echo "extract-ontology-facts: missing config dir: $CONFIG_DIR" >&2
    exit 1
fi

python3 - "$CONFIG_DIR" "$OUTPUT_FILE" <<'PY'
import os
import re
import sys
from datetime import date

config_dir, output_file = sys.argv[1], sys.argv[2]

def read_text(path: str) -> str:
    with open(path, encoding="utf-8") as handle:
        return handle.read()

def file_exists(relative_path: str) -> bool:
    return os.path.isfile(os.path.join(config_dir, relative_path))

def dir_exists(relative_path: str) -> bool:
    return os.path.isdir(os.path.join(config_dir, relative_path))

def function_defined(relative_path: str, function_name: str) -> bool:
    path = os.path.join(config_dir, relative_path)
    if not os.path.isfile(path):
        return False
    text = read_text(path)
    return bool(re.search(rf"^{re.escape(function_name)}\s*\(\)", text, re.M))

def alias_target(relative_path: str, alias_name: str) -> str | None:
    path = os.path.join(config_dir, relative_path)
    if not os.path.isfile(path):
        return None
    text = read_text(path)
    match = re.search(
        rf"alias\s+{re.escape(alias_name)}\s*=\s*'([^']+)'",
        text,
    )
    return match.group(1) if match else None

def env_export_present(relative_path: str, variable_name: str) -> bool:
    path = os.path.join(config_dir, relative_path)
    if not os.path.isfile(path):
        return False
    text = read_text(path)
    return variable_name in text

layout_variant = "sn4_4a" if dir_exists("plugins/verification") else "monorepo"

artifact_paths = [
    "core/path.contract",
    "local/path.contract.example",
    ".path.contract.example",
    "core/tool.contract",
    "plugins/verification",
    "bin/agent-build-layout.sh",
    "bin/agent-verify-layout.sh",
    "bin/agent-test-layout.sh",
    "bin/cockpit-mcp.sh",
    ".agents/verification/cockpit.yaml",
]

functions = [
    ("path_contract_apply", "core/path-resolve.sh"),
    ("path_contract_apply_project", "core/path-resolve.sh"),
    ("path_contract_reassert", "core/path-resolve.sh"),
    ("path_contract_verify", "core/path-resolve.sh"),
    ("path_contract_apply_core_only", "core/path-resolve.sh"),
    ("path_prepend", "core/path.sh"),
    ("verification_script_path", "core/lib.sh"),
    ("verification_lib_dir", "core/lib.sh"),
    ("agent_build", "core/functions.sh"),
    ("agent_verify", "core/functions.sh"),
    ("agent_test", "core/functions.sh"),
    ("verify_workflow_root", "core/functions.sh"),
    ("detect_editor_terminal", "core/lib.sh"),
]

aliases = [
    ("ab", "agent_build"),
    ("av", "agent_verify"),
    ("at", "agent_test"),
]

env_exports = [
    "SHELL_VERIFICATION_ROOT",
    "SHELL_VERIFICATION_BIN",
    "SHELL_VERIFICATION_LIB",
]

lines = [
    "# Generated by bin/extract-ontology-facts.sh — do not hand-edit",
    "meta:",
    f'  extracted_at: "{date.today().isoformat()}"',
    '  layout: plugins_verification',
    f'  layout_variant: "{layout_variant}"',
    f'  shell_root: "{config_dir}"',
    "",
    "artifacts:",
]
for artifact_path in artifact_paths:
    exists = file_exists(artifact_path) or dir_exists(artifact_path)
    lines.append(f"  - path: {artifact_path}")
    lines.append(f"    exists: {'true' if exists else 'false'}")

lines.append("")
lines.append("functions:")
for function_name, relative_file in functions:
    found = function_defined(relative_file, function_name)
    lines.append(f"  - name: {function_name}")
    lines.append(f"    file: {relative_file}")
    lines.append(f"    found: {'true' if found else 'false'}")

lines.append("")
lines.append("aliases:")
for alias_name, expected_target in aliases:
    actual_target = alias_target("core/aliases.sh", alias_name)
    lines.append(f"  - alias: {alias_name}")
    lines.append(f"    expected: {expected_target}")
    lines.append(f"    actual: {actual_target or 'missing'}")
    lines.append(f"    ok: {'true' if actual_target == expected_target else 'false'}")

lines.append("")
lines.append("env_exports:")
for variable_name in env_exports:
    present = env_export_present("core/env.sh", variable_name)
    lines.append(f"  - name: {variable_name}")
    lines.append(f"    found: {'true' if present else 'false'}")

strict_path_found = False
verify_launch_path = os.path.join(config_dir, "plugins/verification/lib/verify-launch.sh")
if os.path.isfile(verify_launch_path):
    strict_path_found = "agent_strict_path_check" in read_text(verify_launch_path)

lines.append("")
lines.append("resolver:")
lines.append("  plugin_bin: plugins/verification/bin")
lines.append("  plugin_lib: plugins/verification/lib")
lines.append(f"  verify_launch: {'true' if file_exists('plugins/verification/lib/verify-launch.sh') else 'false'}")
lines.append(f"  strict_path_helpers: {'true' if strict_path_found else 'false'}")

os.makedirs(os.path.dirname(output_file), exist_ok=True)
with open(output_file, "w", encoding="utf-8") as handle:
    handle.write("\n".join(lines) + "\n")

print(output_file)
PY
